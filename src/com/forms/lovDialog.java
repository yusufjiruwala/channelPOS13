    @Override
    public void setVisible(boolean b) {
        try {
            if (b) {
                selectedno = -1;
                setupLov();
            }
        } catch (SQLException ex) {
            Logger.getLogger(lovDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
        super.setVisible(b);

    }

    public void setDbc(DBClass dbc) {

        this.dbc = dbc;
    }

    public selectListView getSlov() {
        return slov;
    }

    public void setupLov() throws SQLException {
        if (sqlstr != null && sqlstr.length() > 0) {
            dbc = slov.getLctb().createDBClassFromConnection(dbc.getDbConnection());
            slov.getLctb().executeQuery(sqlstr, true);
        }
        slov.getLctb().setEditAllowed(false);
        slov.setParentPanel(jPanel1);
        slov.setParentJf(this);
        slov.setSelectionStyle(2);
        slov.createView();
        repaint();
        setLocationRelativeTo(null);
    }

    public void setSelectedNo(int no) {
        this.selectedno = no;
        setVisible(false);
    }

    public void setwidth(int no) {
        setPreferredSize(new Dimension(no, getHeight()));
        pack();
    }

    public static lovDialog getInstance(String sql) throws SQLException {
        return getInstance(utils.getMainFrame(), sql, true, false);
    }

    public static lovDialog getInstance(Component par, String sql, int width, int height, boolean show, boolean auto_size_cols) throws SQLException {
        lovDialog ld = getInstance(par, sql, false, auto_size_cols);
        ld.getSlov().setAutoSize(auto_size_cols);
        ld.setSize(width, height);
        ld.setLocationRelativeTo(null);
        if (show) {
            ld.setVisible(true);
        }
        return ld;
    }

    public static lovDialog getInstance(Component par, String sql, boolean show, boolean auto_size_cols) throws SQLException {
        lovDialog ld = null;
        if (par instanceof Frame) {
            ld = new lovDialog((Frame) par, true);
        }
        if (par instanceof Dialog) {
            ld = new lovDialog((Dialog) par, true);
        }

        ld.getSlov().getLctb().createDBClassFromConnection(utils.getMainFrame().getDbConneciton());
        ld.setNoOfClicksOnSelection(selectListView.DOUBLE_CLICK_SELECTION);
        ld.getSlov().getLctb().executeQuery(sql, true);
        ld.getSlov().getLctb().setMasterRowsAsRows();
        if (show) {
            ld.setVisible(true);
        }
        return ld;
    }
